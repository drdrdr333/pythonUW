def add(_a: int, _b: int) -> int:
    """
    Quick function for adding two numbers
    """
    adder = [_a, _b]
    return sum(adder)


def subtract(_a: int, _b: int) -> int:
    """
    Quick function for subtraction of two numbers
    """
    return _a - _b


def multiply(_a: int, _b: int) -> int:
    """
    Quick function for multiplication of two numbers
    """
    return _a * _b


